package com.mycompany.genericsprogram;

public class Printer {

    // Method to print Character Array elements
    public static void printArrayCharacter(Character[] inputArray) {
        // Display array elements
        for (Character element : inputArray) {
            System.out.printf("%s ", element);
        }
        System.out.println();
    }

    // Method to print String Array elements
    public static void printArrayString(String[] inputArray) {
        // Display array elements
        for (String element : inputArray) {
            System.out.printf("%s ", element);
        }
        System.out.println();
    }

    // Method to print Double Array elements
    public static void printArrayDouble(Double[] inputArray) {
        // Display array elements
        for (Double element : inputArray) {
            System.out.printf("%s ", element);
        }
        System.out.println();
    }

    // Method to print Integer Array elements
    public static void printArrayInt(Integer[] inputArray) {
        // Display array elements
        for (Integer element : inputArray) {
            System.out.printf("%s ", element);
        }
        System.out.println();
    }
}
